			<p>Works with Google Slides, PowerPoint, KeyNote, Open Office and more.</p>

			<hr class="uk-divider-icon">

			<h3>Why does this tool exist?</h3>
			<p>Too often we have seen online courses that are just a link to a powerpoint file and a separate link to the recording of the presentation - be it audio or video recorded on a phone as the lecture or presentation proceeds. It is then up to the learner to begin running the audio file while they page through the prentation hoping to follow along. <em>Assuming</em> they even have Powerpoint on their computer.</p>

			<p>We will convert the presentation to a web-friendly format that works in web browsers on desktop or tablet or mobile, and <em>synchronise</em> audio to the slides so that as the user skips about between slides, the audio keeps sync with that part of the presesentation. Or the user can simply listen along and the slides will turn automatically where appropriate. This is <em>super user-friendly</em>.</p>

			<p>Plus, you can specify which slide will perform a SCORM completion. We mentioned that the package your produce is SCORM-compliant right? Well you can track how far through your learners got. That's also <em>super handy</em> for trainers of all kinds. But you can also ignore the tracking stuff and make a regular package too.</p>

			<hr class="uk-divider-icon">

			<h3>How does this work?</h3>

			<p>First up you need to upload your presentation, which we will convert to HTML5 for you. Next you can record your audio directly to the browser, or if you already have a recording you can upload that too. We will convert it to web audio format if need be.</p>

			<p>Next we'll cue your audio based on the number of slides in the presentation. It's likely that these slide change markers (cue points) won't suit the natural points described in your audio, so you can move the cue points about and play it through to see the slides change as the audio plays.</p>

			<p>Lastly you can choose a simple design for the output and download your package. It's pretty simple on the surface (there's lots going on underneath that you really don't need to worry about).</p>

			<hr class="uk-divider-icon">

